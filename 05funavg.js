
function average(a,b){
    return a+b/2;
}
console.log(average(2, 1));
//2.5
//The function `average(a, b)` takes two arguments and returns their average. 
//In this case, the arguments are `2` and `1`, so the function returns `(2 + 1) / 2`, which is `1.5`. 
//Therefore, the output of the `console.log()` statement is `2.5`.